//用于食物与失败条件的判断

int Panduan(struct SHE *head, int x, int y)
{
    int flag = 1;
    if (x < 0 || x > HENG - 1 || y < 0 || y > SHU - 1)
    {
        return 0;  //撞墙
    }
    while (head != NULL)
    {
        if (head -> x == x && head -> y == y && flag != 1)
        {
            return 0; //咬到自己
        }
        flag++;
        if (head -> next == NULL)
        {
            Tx = head -> x;
            Ty = head -> y;
        }
        head = head -> next;
    }
    if (x == Fx && y == Fy)
    {
        return 1;//有食物
    }
    return 2;//无异常
}

//蛇身增长

struct SHE *Link(struct SHE *head)
{
    struct SHE *p, *pr;
    pr = head;
    p = (struct SHE *)malloc(sizeof(struct SHE));
    if (p == NULL)
    {
        printf("发生错误！！！");
        exit(0);
    }
    if (head == NULL)
    {
        head = p;
    }
    else
    {
        while (pr -> next != NULL)
        {
            pr = pr -> next;
        }
        pr -> next = p;
    }
    p -> x = Tx;
    p -> y = Ty;
    p -> next = NULL;
    return head;
}



